    // Constructor.
    public SVGColor() {

        set(0, 0, 0, 0);
    }

    // Set constructor.
    public SVGColor(float r, float g, float b) {

        set(r, g, b, 1);
    }

    public SVGColor(float r, float g, float b, float a) {

        set(r, g, b, a);
    }

    public SVGColor(SVGColor src) {

        set(src);
    }

    public void set(SVGColor src) {

        _r = src._r;
        _g = src._g;
        _b = src._b;
        _a = src._a;
    }

    private void set(float r, float g, float b, float a) {

        // clamp each component in the [0; 1] range
        _r = (r < 0.0f) ? 0.0f : (r > 1.0f) ? 1.0f : r;
        _g = (g < 0.0f) ? 0.0f : (g > 1.0f) ? 1.0f : g;
        _b = (b < 0.0f) ? 0.0f : (b > 1.0f) ? 1.0f : b;
        _a = (a < 0.0f) ? 0.0f : (a > 1.0f) ? 1.0f : a;
    }

    public float getRed() {

        return _r;
    }

    public float getGreen() {

        return _g;
    }

    public float getBlue() {

        return _b;
    }

    public float getAlpha() {

        return _a;
    }

    private float _r;
    private float _g;
    private float _b;
    private float _a;
